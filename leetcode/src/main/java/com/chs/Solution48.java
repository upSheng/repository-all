public class Solution48 {

    public static void main(String[] args) {

        int[][] matrix ={{1,2},{3,1}};
        rotate(matrix);

        System.out.println();
    }

    public static void rotate(int[][] matrix) {

        int n = matrix.length;


        for (int i = 0; i < n; i++) {
            for (int j = i; j < n; j++) {
                int tmp = matrix[j][i];
                matrix[j][i] = matrix[i][j];
                matrix[i][j] = tmp;
            }
        }

        for (int i = 0; i < n; i++) {
            for (int j = 0; j < n / 2; j++) {
                int tmp = matrix[i][j];
                matrix[i][j] = matrix[i][n - j - 1];
                matrix[i][n - j - 1] = tmp;
            }
        }


    }
}
